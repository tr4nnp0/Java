
public class TesteSwitch1 {

	public static void main(String[] args) {
		int num = 5;
		switch(num) {
		case 1:
			System.out.println("variável número igual a 1");
			break;
		case 2:
			System.out.println("variável número igual a 2");
			break;
		default:
			System.out.println("variável número é diferente de 1 e 2");
		}
	}
}
